def in_range(y, x):
    return 0 <= y < N and 0 <= x < M

def get_max_sum(y, x):
    max_sum = 0
    for shape in shapes:
        current_sum = 0
        for dy in range(4):
            for dx in range(4):
                if in_range(y + dy, x + dx) and shape[dy][dx]:
                    current_sum += paper[y + dy][x + dx]

        max_sum = max(max_sum, current_sum)
    return max_sum

max_value = 0

for y in range(N):
    for x in range(M):
        max_value = max(max_value, get_max_sum(y, x))

print(max_value)

